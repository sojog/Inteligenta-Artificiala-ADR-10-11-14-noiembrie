
## Python este un limbaj loosly typed - o variabila poate lua diferite tipuri
## Tipuri de date de baza

## INTEGER
x = 2
print(x, type(x))  # int

## FLOATING NUMBER
x = 2.032
print(x, type(x))  # float

## BOOLEAN
x = True
print(x, type(x))  # bool

## STRING
x = "hello"
print(x, type(x))  # str

## STRING
x = 'hello'
print(x, type(x))  # str

## STRING 
x = """hello"""  ## folosite la documentatie
print(x, type(x))  # str